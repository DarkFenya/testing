def _clean_triggers(problem_types: Dict[str, ProblemType]) -> None:
    for type_key, type_info in problem_types.items():
        seen = set()
        unique_triggers: List[str] = []
        for trigger in type_info.triggers:
            normalized = trigger.strip().lower()
            if normalized in seen:
                continue
            seen.add(normalized)
            unique_triggers.append(trigger)
        unique_triggers.sort()
        problem_types[type_key].triggers = unique_triggers


def _pattern_from_trigger(trigger: str, type_key: str) -> Pattern[str]:
    trigger = trigger.strip()
    punctuation_only = trigger and all(char in "!?" for char in trigger)

    if punctuation_only:
        unique_chars = "".join(sorted(set(trigger)))
        return re.compile(rf"[{re.escape(unique_chars)}]{{2,}}")

    if type_key == "type2" and "*" in trigger:
        pattern_body = re.escape(trigger).replace(r"\*", r"[\w\d]*")
        return re.compile(rf"(?i)(?<![\w]){pattern_body}(?![\w])")

    escaped = re.escape(trigger)
    return re.compile(rf"(?i)(?<![\w]){escaped}(?![\w])")


def _compile_patterns(problem_types: Dict[str, ProblemType]) -> None:
    for type_key, type_info in problem_types.items():
        patterns: List[Pattern[str]] = []
        seen = set()
        for trigger in type_info.triggers:
            pattern = _pattern_from_trigger(trigger, type_key)
            pattern_str = pattern.pattern
            if pattern_str in seen:
                continue
            seen.add(pattern_str)
            patterns.append(pattern)
        problem_types[type_key].patterns = patterns


def build_problem_types() -> Dict[str, ProblemType]:
    """Return initialized, deduplicated, and compiled problem types."""

    problem_types = {key: ProblemType(info.name, list(info.triggers)) for key, info in _BASE_PROBLEM_TYPES.items()}
    _add_advanced_triggers(problem_types)
    _clean_triggers(problem_types)
    _compile_patterns(problem_types)
    return problem_types

